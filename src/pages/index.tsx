import { SignOutButton } from "@clerk/clerk-react";
import { useUser } from "@clerk/nextjs";
import { type NextPage } from "next";
import Head from "next/head";
import { api } from "~/utils/api";
import type { RouterOutputs } from "~/utils/api";
import dayjs from "dayjs";
import relativeTime from "dayjs/plugin/relativeTime";
import Image from "next/image";

dayjs.extend(relativeTime);

const Home: NextPage = () => {
  const user = useUser();

  const { data, isLoading, error } = api.post.getAll.useQuery();

  if (isLoading) return <span>Loading...</span>;

  if (error) return <span>Something went wrong...</span>;

  const CreatePostWizard = () => {
    const { user } = useUser();

    if (!user) return null;

    return (
      <>
        <div className="flex h-full w-full items-center gap-2">
          <Image
            className="h-14 w-14 rounded-full"
            src={user.profileImageUrl}
            alt="My profile Image"
            width={56}
            height={56}
            priority
          />
          <input
            placeholder="Share your thoughts..."
            className="grow bg-transparent outline-none"
            name="post"
          />
        </div>
      </>
    );
  };

  type postWithUser = RouterOutputs["post"]["getAll"][number];
  const PostView = (props: postWithUser) => {
    const { post, author } = props;
    return (
      <div
        className="flex w-full gap-2 border-b border-slate-400 p-4 "
        key={post.id}
      >
        <Image
          className="h-14 w-14 rounded-full"
          src={author.profileImageUrl}
          alt={`@${author.userName}'s profile image`}
          width={56}
          height={56}
          priority
        />
        <div className="flex grow flex-col">
          <p className="text-slate-500">
            @{author.userName}
            {" Â· "}
            <span className="opacity-50">
              {dayjs(post.createdAt).fromNow()}
            </span>
          </p>
          <p>{post.content}</p>
        </div>
      </div>
    );
  };
  return (
    <>
      <Head>
        <title>Chirp - Twitter clone</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex justify-center px-1 md:px-0">
        <div className="min-h-screen w-full max-w-3xl border-x border-slate-400">
          <div className="border-b border-slate-400 p-4">
            {!!user.isSignedIn && <SignOutButton className="mb-2" />}
            <CreatePostWizard />
          </div>
          <div className="flex-column flex">
            {data.map((post) => (
              <PostView {...post} key={post.post.id} />
            ))}
          </div>
        </div>
      </main>
    </>
  );
};

export default Home;
